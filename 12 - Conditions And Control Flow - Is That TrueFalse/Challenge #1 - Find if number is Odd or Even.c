#include <stdio.h>

/**
 * main - Entry point of the program
 *
 * Description: This function checks if a number is even or odd and prints the result.
 *
 * Return: Always returns 0 to indicate successful execution.
 */

int main(void)
{
	int X;

	printf("enter your number : \n");
	scanf("%d", &X);
	if (X % 2 == 0)
	{
		printf("%d is an even", X);
	}
	else
	{
		printf("%d is an even", X);
	}

	return (0);
}
